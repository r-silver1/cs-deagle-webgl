function retBoard(l){
    var retVal = new Array();
    var maxHeight = 3;
    for(var i =0; i< l; i+=1){
        retVal.push([]);
        for(var j =0; j < l; j+=1){
            var ranVal = Math.round(Math.random());
            if(ranVal){
                retVal[i].push(Math.round(Math.random()*maxHeight));
            }else{
                retVal[i].push(0);
            }
        }
    }
    return retVal;
}

